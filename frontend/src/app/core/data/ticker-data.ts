export function getTickersByCategory(category: 'sp500' | 'forex' | 'etf'): TickerInfo[] {
  switch (category) {
    case 'sp500': return SP500_TICKERS;
    case 'forex': return FOREX_TICKERS;
    case 'etf': return ETF_TICKERS;
  }
}

export function searchTickers(query: string, category?: 'sp500' | 'forex' | 'etf'): TickerInfo[] {
  const q = query.toLowerCase();
  const source = category ? getTickersByCategory(category) : [...SP500_TICKERS, ...FOREX_TICKERS, ...ETF_TICKERS];
  return source.filter(t =>
    t.symbol.toLowerCase().includes(q) ||
    t.name.toLowerCase().includes(q) ||
    (t.sector && t.sector.toLowerCase().includes(q))
  );
}

export function getSectors(): string[] {
  const sectors = new Set(SP500_TICKERS.map(t => t.sector).filter((s): s is string => !!s));
  return Array.from(sectors).sort();
}

export function getTickersBySector(sector: string): TickerInfo[] {
  return SP500_TICKERS.filter(t => t.sector === sector);
}
